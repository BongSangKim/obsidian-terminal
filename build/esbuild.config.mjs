import esbuild from "esbuild"
import process from "process"
import builtins from "builtin-modules"

const comment = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD.
If you want to view the source, please visit the repository of this plugin.
*/`
const production = process.argv[2] === "production"

await esbuild.build({
	banner: { js: comment, },
	bundle: true,
	color: true,
	drop: [],
	entryPoints: ["sources/main.ts"],
	external: [
		"electron",
		"obsidian",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	footer: { js: comment, },
	format: "cjs",
	incremental: false,
	jsx: "transform",
	legalComments: "inline",
	loader: { ".py": "text", },
	logLevel: "info",
	logLimit: 0,
	minify: production,
	outfile: "main.js",
	platform: "browser",
	sourcemap: production ? false : "inline",
	sourcesContent: true,
	target: "es2018",
	treeShaking: true,
	watch: !production,
}).catch(() => process.exit(1))
