import builtins from "builtin-modules"
import esbuild from "esbuild"
import process from "process"

const ARGV_PRODUCTION = 2,
	COMMENT = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD.
If you want to view the source, please visit the repository of this plugin.
*/`,
	production = process.argv[ARGV_PRODUCTION] === "production"

await esbuild.build({
	banner: { js: COMMENT },
	bundle: true,
	color: true,
	drop: [],
	entryPoints: ["sources/main.ts"],
	external: [
		"electron",
		"obsidian",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		"node:*",
		...builtins,
	],
	footer: { js: COMMENT },
	format: "cjs",
	incremental: false,
	jsx: "transform",
	legalComments: "inline",
	loader: { ".py": "text" },
	logLevel: "info",
	logLimit: 0,
	minify: production,
	outfile: "main.js",
	platform: "browser",
	sourcemap: production
		? false
		: "inline",
	sourcesContent: true,
	target: "ES2018",
	treeShaking: true,
	watch: !production,
})
